cars = ["BMW","Porshe","Bugatti","Rolls Royce","Audi",'Mercedes','VW','Maclaren']
print(cars)

#Returns the first 3 items
print(cars[0:3])

#Returns items within the numbers
print(cars[1:5])

#Returns the first 4 items ignoring [0]
print(cars[:4])

#Returns everything other than the the first 2 items
print(cars[2:])

#copying list
vehicles = cars[:]
print(vehicles)
